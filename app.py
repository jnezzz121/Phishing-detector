# 2. ФУНКЦИЯ ИЗВЛЕЧЕНИЯ ПРИЗНАКОВ (9 признаков)
# ============================================================
def extract_features(url):
    """Извлекает 9 признаков из URL (соответствует обучению модели)"""
    url_lower = url.lower()
    
    # 1. Длина URL
    length = len(url)
    
    # 2. Доля цифр
    digit_count = sum(c.isdigit() for c in url)
    digit_ratio = digit_count / length if length > 0 else 0
    
    # 3. Наличие HTTPS
    has_https = 1 if url.startswith('https') else 0
    
    # 4. Наличие символа @
    has_at = 1 if '@' in url else 0
    
    # 5. Наличие IP-адреса вместо домена
    has_ip = 1 if re.search(r'\d+\.\d+\.\d+\.\d+', url) else 0
    
    # 6. Количество подозрительных слов
    suspicious_words = ['secure', 'verify', 'login', 'account', 'confirm', 'update', 
                        'signin', 'auth', 'authenticate', 'validate', 'restore']
    sus_count = sum(1 for w in suspicious_words if w in url_lower)
    
    # 7. Сокращённая ссылка
    shorteners = ['bit.ly', 'goo.gl', 'tinyurl.com', 'is.gd', 'ow.ly', 'click.ru', 
                  'clck.ru', 'short.link', 'rb.gy', 'cutt.ly', 't.co']
    is_short = 1 if any(s in url_lower for s in shorteners) else 0
    
    # 8. Количество специальных символов
    special_chars = sum(1 for c in url if c in './:@-?_~#&=%+')
    
    # 9. Количество поддоменов (точек в домене)
    subdomain_count = url_lower.count('.')
    
    return [length, digit_ratio, has_https, has_at, has_ip, sus_count, is_short, special_chars, subdomain_count]

def predict_url(url):
    """Предсказание с использованием обученной модели"""
    features = extract_features(url)
    features_array = np.array(features).reshape(1, -1)
    
    if model is not None:
        proba = model.predict_proba(features_array)[0][1] * 100
    else:
        # fallback (упрощённая эвристика, если модель не загрузилась)
        proba = 50
        if len(url) > 75:
            proba += 20
        if not url.startswith('https'):
            proba += 20
        if '@' in url:
            proba += 25
        if re.search(r'\d+\.\d+\.\d+\.\d+', url):
            proba += 30
        proba = min(proba, 99)
    
    result = 'phishing' if proba > 50 else 'safe'
    return result, proba

